					<p align="justify"><font size="3">
					<ol>
						<li><b>Kunyit</b></li>
						<p align="justify"><font size="3">Kunyit merupakan salah satu jenis rempah berbentuk rizoma yang paling umum digunakan di dapur Indonesia. Berbeda dengan masakan India yang menggunakan kunyit bubuk, masakan Indonesia selalu menggunakan kunyit segar yang dihaluskan sebagai bumbu. Rasanya sudah pasti jauh berbeda karena lebih segar dan juga lebih wangi.</font></p>
						<li><b>Jahe</b></li>
						<p align="justify"><font size="3">Bersama kunyit, jahe merupakan salah satu jenis rempah rizoma yang sangat khas penggunaannya dalam masakan Indonesia. Wanginya yang hangat menjadikannya sering kali tak hanya dibuat bahan masakan, tetapi juga minuman tradisional.</font></p>
						<li><b>Lengkuas</b></li>
						<p align="justify"><font size="3">Lengkuas atau laos merupakan jenis rempah sangat unik dengan aroma khas. Lengkuas sangat cocok digunakan sebagai bumbu untuk menumis dan membuat berbagai masakan Nusantara. Bersama daun salam, kedua rempah ini merupakan pasangan yang sangat serasi.</font></p>
						<li><b>Kencur</b></li>
						<p align="justify"><font size="3">Di antara semua jenis rempah rizoma, kencur adalah yang paling kecil, tetapi aromanya sangat kuat. Kencur biasanya digunakan sebagai bahan jamu dan masakan khas Sunda yang nikmat.</font></p>
						<li><b>Daun Salam</b></li>
						<p align="justify"><font size="3">Di antara semua jenis rempah daun, daun salam yang paling mencirikan masakan Indonesia. Sejak dahulu tanaman ini tumbuh subur di pekarangan Nusantara. Wanginya sangat khas, apalagi jika sudah dimasak bersama lengkuas dan rempah lainnya.</font></p>
						<li><b>Bawang Merah & Bawang Putih</b></li>
						<p align="justify"><font size="3">Bawang merah dan bawang putih sangatlah penting dalam dapur Indonesia. Saking pentingnya, sampai-sampai dibuat kisah klasik mengenai kedua bawang tersebut. Menumis dengan bawang merah dan bawang putih lokal akan memberikan hasil jauh berbeda daripada bawang impor. Bawang merah dan bawang putih lokal berukuran lebih kecil, tetapi rasanya sangat tajam dan gurih.</font></p>
						<li><b>Cabai</b></li>
						<p align="justify"><font size="3">Makanan Indonesia identik dengan rasa pedas. Di Indonesia sendiri berbagai jenis cabai biasa digunakan, ada cabai rawit merah dan hijau, cabai keriting, cabai merah dan hijau besar, hingga cabai gendot.</font></p>
						<li><b>Kemiri</b></li>
						<p align="justify"><font size="3">Kemiri dan pala merupakan rempah khas dari Maluku. Kemiri memiliki bentuk seperti kacang macadamia dan juga warna khas seperti kacang pucat. Kemiri sendiri tidak memiliki bau dan rasa, tetapi jika sudah dimasak, aromanya akan keluar dengan sendirinya.</font></p>
						<li><b>Pala</b></li>
						<p align="justify"><font size="3">Pala sudah ratusan tahun menjadi ikon Maluku. Pala tidak bisa digunakan saat masih berbentuk biji, jadi harus diparut atau diulek halus. Wangi pala sangat khas, mirip campuran kayu manis dan cengkih.</font></p>
						<li><b>Terasi</b></li>
						<p align="justify"><font size="3">Terasi merupakan bahan masakan yang sangat khasi Indonesia, terutama di daerah Cirebon dan Madura. Terasi merupaka hasil fermentasi udang dan ikan. Baunya yang sangat khas menjadikannya digemari sebagian orang, tetapi juga dijauhi sebagian lainnya. Bagi mereka yang menyukainnya, terasi sungguhlah nikmat, apalagi jika dicampur dengan sambal.</font></p>
						<li><b>Serai</b></li>
						<p align="justify"><font size="3">Serai atau biasa disebut dengan <i>lemongrass</i> (karena wanginya mirip dengan jeruk) memiliki peranan cukup penting dalam masakan Indonesia. Serai paling banyak digunakan di daerah Manado dan daerah pesisir laut lainnya. Wanginya yang khas menjadikannya bumbu wajib di Indonesia.</font></p>
						<li><b>Bunga Kecombrang</b></li>
						<p align="justify"><font size="3">Kecombrang merupakan jenis bunga paling umum digunakan untuk kuliner di Indonesia. Rasa asam dan wangi khas kecombrang merupakan alasan mengapa arsik ikan mas khas Batak sangat digemari banyak orang.</font></p>
						<li><b>Daun Jeruk</b></li>
						<p align="justify"><font size="3">Daun jeruk purut merupakan salah satu jenis daun paling penting bagi dapur Indonesia. Daun jeruk memiliki beragam fungsi dalam berbagai masakan daerah, dari masakan Padang hingga Manado. Wanginya yang harum segar menjadikannya rempah yang harus selalu tersedia di rumah.</font></p>
						<li><b>Keluwak</b></li>
						<p align="justify"><font size="3">Keluwak atau kapayang dalam bahasa Melayu merupakan salah satu bumbu khas Indonesia. Keluwak harus diolah dengan cara tertentu agar tidak salah penggunaannya. Masakan berciri khas keluwak adalah rawon khas Jawa Timur.</font></p>
						<li><b>Petis</b></li>
						<p align="justify"><font size="3">Lain dengan terasi yang juga dikenal di berbagai negara Asia Tenggara lainnya, petis hanya dikenal di Indonesia. Petis bisa dibuat dari berbagai macam ekstraksi bahan, dari sapi hingga udang yang difermentasi dan diberi gula batok. Rasanya yang khas menjadikan petis favorit banyak orang.</font></p>
