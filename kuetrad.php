					<p align="justify"><font size="3">Kalau mau dikelompokkan, kue tradisional bisa dibagi berdasarkan cara penyelesaian kue tersebut. Misalnya kue tradisional dikukus, direbus, dipanggang, dan digoreng. Jadi tak aneh jika jenisnya menjadi banyak dan variatif. Karena penyelesaiannya yang berbeda-beda itu pula, maka rasa tiap kue tak sama. Meski sama-sama dikukus, perpaduan bahannya berbeda. Belum lagi penggunaan bahan pendamping atau pengharumnya.</font></p>
					<p align="justify"><font size="3">Pemilihan bahan dan komposisi yang tepat menentukan kelezatan kue tradisional yang dibuat. Bahan kue tradisional bisa kita kelompokkan sebagai berikut :</font></p>
					<h3>Bahan Utama</h3>
					<p align="justify"><font size="3">
					<ol>
						<li><b>Tepung Beras & Beras</b></li>
						<p align="justify"><font size="3">Tepung beras dibuat dari beras yang ditumbuk sampai halus. Tidak perlu menumbuk sendiri karena di toko tepung beras mudah diperoleh. Yang penting lihat tanggal kadaluarsa supaya tepung tidak berbau. Panganan yang dibuat dari tepung beras antara lain, kue pisang, kue serabi, dan kue talam hijau. Beras juga digunakan untuk membuat penganan tradisional. Misalnya, buras atau arem-arem.</font></p>
						<li><b>Tepung Ketan & Beras Ketan</b></li>
						<p align="justify"><font size="3">Tepung ketan dibuat dari beras ketan yang digiling halus. Tepung ketan biasa dibuat jadi gemblong dan bugis. Sementara beras ketan biasanya dibikin lemper, lopis, dan lalampa. Beras ketan hitam juga sering digunakan untuk membuat kue tradisional kita.</font></p>
						<li><b>Tepung Lain</b></li>
						<p align="justify"><font size="3">Tepung lain yang sering digunakan adalah tepung kanji, tepung terigu, dan tepung hunkwe.</font></p>
						<li><b>Santan</b></li>
						<p align="justify"><font size="3">Santan dibuat dengan kelapa parut yang diperas. Jumlah kelapa yang digunakan menentukan kekentalan dan keenceran santan. Santan sebaiknya diperoleh dari kelapa parut yang diseduh air panas. Santan juga bisa didapat dari santan siap pakai yang dijual dalam kemasan. Untuk menentukan kekentalan dan keencerannya, ikuti saran kemasan.</font></p>
						<li><b>Gula</b></li>
						<p align="justify"><font size="3">Gula yang digunakan untuk membuat kue tradisional adalah gula pasir dan gula merah.</font></p>
						<li><b>Umbi-umbian</b></li>
						<p align="justify"><font size="3">Umbi-umbian seperti ubi, singkong, talas, dan kentang bisa sebagai bahan utama, bisa juga sebagai bahan tambahan di dalam kue tradisional. Ubi, misalnya sebagai bahan utama dijadikan kue talam. Tetapi bisa juga jadi bahan tambahan atau pelengkap. Biasanya sebagai bahan tambahan atau pelengkap, umbi-umbian direbus lalu dipotong kotak. Sedangkan sebagai bahan utama, umbi-umbian diparut atau dihaluskan setelah dikukus.</font></p>
						<li><b>Pengembang</b></li>
						<p align="justify"><font size="3">Untuk mengembangkan kue-kue tradisional fermentasi digunakan tape singkong, tuak, dan ragi (biasanya digunakan ragi instan untuk kepraktisan). Umumnya bahan pengembang ini dibuat jadi biang dulu. Pembuatan biangnya pun dibiarkan sejenak (difermentasi).</font></p>
					</ol>
					</font></p>
					
					<h3>Bahan Pendamping</h3>
					<p align="justify"><font size="3">Bahan pendamping atau tambahan biasanya digunakan sebagai si kue atau sekadar memberi variasi. Tujuannya selain menambah kelezatan juga untuk mempercantik penampilan kue.
					<ol>
						<li><b>Kelapa Parut</b></li>
						<p align="justify"><font size="3">Untuk membuat santan kelapa biasanya diparut halus. Tetapi kelapa untuk isi harus diparut lebih kasar hingga hasilnya lebih enak.</font></p>
						<li><b>Kelapa Muda, Pisang, Nangka, Kacang Tolo, dan Pacar Cina</b></li>
						<p align="justify"><font size="3">Kelapa muda biasa ditambahkan dalam kue tradisional kita seperti kue lumpur atau jogkong. Fungsi penambahan tersebut lebih untuk mempermanis kue. Khusus untuk nangka, fungsinya tak cuma tambahan, tetapi juga mengharumkan kue.</font></p>
						<li><b>Kacang Hijau</b></li>
						<p align="justify"><font size="3">Kacang hijau bisa digunakan untuk isi kue seperti kue onde-onde atau kue tradisional lain.</font></p>
					</ol>
					</font></p>
					
					<h3>Bahan Pengharum</h3>
					<p align="justify"><font size="3">Kue tradisional tentu menjadi jauh lebih enak kalau diberikan tambahan pengharum. Nah, inilah pengharum yang biasa digunakan dalam kue tradisional.</font></p>
					<ol>
						<li><b>Daun Pandan</b></li>
						<p align="justify"><font size="3">Keharuman pandan memang luar biasa dan sungguh pas dengan kue tradisional. Biasanya pandan dimasak dalam santan, adonan isi, atau sekadar diletakkan di dasar cetakan.</font></p>
						<li><b>Daun Jeruk & Daun Salam</b></li>
						<p align="justify"><font size="3">Daun jeruk dan daun salam juga amat disukai orang untuk memberi keharuman khusus pada kue tradisional. Begitu pula dengan kulit jeruk purut.</font></p>
						<li><b>Vanili</b></li>
						<p align="justify"><font size="3">Umumnya orang lebih suka vanili bubuk. Namun sebagian orang lagi memilih memakai vanili segar yang diambil langsung dari batangnya karena dinilai lebih harum.</font></p>
					</ol>
					</font></p>
					
					<h3>Bahan Pewarna</h3>
					<p align="justify"><font size="3">Pewarna makanan biasanya menggunakan pewarna buatan. Padahal dari dulu nenek moyang kita lebih suka memakai pewarna alami seperti kunyit (untuk kuning), daun suji (untuk hijau), dan angkak (untuk merah). Hasilnya memang belum tentu seindah pewarna buatan, tetapi pasti dijamin lebih sehat.</font></p>
					
					<h3>Tips Kue Tradisional</h3>
					<ol>
						<li><p align="justify"><font size="3">Pemilihan bahan harus bagus kualitasnya. Baui tepung. Hati-hati tepung beras dan tepung ketan mudah sekali berbau terutama karena penyimpanan yang tidak tepat.</font></p></li>
						<li><p align="justify"><font size="3">Santan sebagai bahan utama kue tradisional harus diperas dengan benar. Kelapa tua menghasilkan santan yang kental dan berminyak. Untuk menghasilkan 200 ml santan dari 1 buah kelapa, tambahkan 150-170 ml air panas ke dalam 1 butir parutan kelapa. Aduk dengan sendok kayu sambil ditekan-tekan lalu peras setelah kelapa hangat. Air panas yang digunakan juga membuat kue tidak mudah basi.</font></p></li>
						<li><p align="justify"><font size="3">Santan akan membuat kue lebih harum bila direbus dengan daun pandan atau bumbu dedaunan yang lain. Selama merebus, api jangan terlalu besar. Aduk santan supaya tidak terpisah antara lemak dan airnya.</font></p></li>
						<li><p align="justify"><font size="3">Pengulenan tepung bisa dilakukan dengan larutan (santan/gula merah/air) panas, hangat, atau dingin. Pengulenan harus dilakukan dengan tangan agar kelembutannya bisa dipantau. Pengadukan dengan tangan juga membuat kita mudah mendeteksi bila ada adonan yang masih menggumpal. </font></p></li>
						<li><p align="justify"><font size="3">Setiap kali adonan akan dimasukkan dalam cetakan, sebaiknya diaduk kembali karena setelah adonan dibiarkan agak lama, tepung akan mengendap ke bawah.</font></p></li>
						<li><p align="justify"><font size="3">Waktu fermentasi harus tepat dan benar. Fermentasi yang terlalu lama akan membuat kue terasa asam. Pori-porinya juga akan menjadi besar.</font></p></li>
						<li><p align="justify"><font size="3">Pengopyokan maksudnya memukul-mukul adonan. Pada adonan yang padat, dilakukan dengan cara agak dibanting. Lakukan pengopyokan juga dengan waktu yang tepat dan kekuatan yang cukup. Pengopyokan yang kurang membuat kue tidak bersarang. Pada kue mangkok, pengopyokan yang kurang membuat kue tidak mekar.</font></p></li>
						<li><p align="justify"><font size="3">Kue tertentu menghendaku cetakan yang panas. Untuk kue yang dipanggang di atas api, selama dipanaskan, cetakan bisa dikasih kemiri yang sudah ditumbuk kasar atau kelapa parut. Aduk sesekali. Api sebaiknya tidak terlalu besar.</font></p></li>
					</ol>
					</font></p>
					
