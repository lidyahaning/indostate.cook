					<center><img src="img/hidangan.jpg" width="600" height="300"></center><br>
					<p align="justify"><font size="3">Indonesia memiliki banyak pulau, provinsi dan kota yang membuat perbendaharaan kuliner sangat melimpah. Bisa saja kita menemukan hidangan yang sama dari beberapa daerah tetapi dengan nama, bahan dan cara yang berbeda. Hidangan bisa datang dari negara lain, namun tidak ditiru begitu saja. Rasa, bahan dan cara memasak disesuaikan dengan selera masyarakat Indonesia. Karena itulah hidangan-hidangan tradisional sangat melimpah. </font></p>
					<h3>racikan bumbu</h3>
					<p align="justify"><font size="3">Tak bisa dipungkiri bumbu yang tumbuh di daerah tertentu akan mempengaruhi racikan bumbu sebuah masakan. Bumbu-bumbu yang ada dilengkapi dengan selera pribadi sehingga menghasilkan masakan lezat yang unik. Beberapa masyarakat masih suka menambahkan racikan bumbunya dengan dedaunan pengharum seperti daun pandan, daun salam koja, dan daun kemangi. Orang Manado sangat menyukai cabai rawitnya. Orang Toraja yang juga suka pedas mengandalkan cabai mereka yang lebih pedas lagi. </font></p>
					<p align="justify"><font size="3">Dalam memasak, bumbu bisa dihaluskan, diiris, atau sekedar dimemarkan. Orang Palembang menggunakan bawang putih yang diparut, bukan dicincang atau dihaluskan. Orang Padang menggunakan alat tertentu untuk memarut kelapa atau bahkan memerasnya menjadi santan. Bahan yang digunakan pun beragam. Bisa saja sama, tetapi berbeda jenis. Misal, jagung di Pulau Jawa berbeda dengan jagung di buton. Salah satu faktor yang mempengaruhi perbedaan tersebut adalah iklim pada daerah tersebut. Semua itu menyebabkan banyaknya jumlah kuliner Indonesia.</font></p>
					<h3>Teknik memasak</h3>
					<p align="justify"><font size="3">Teknik masak kuliner Indonesia sangat menyeluruh. Dari yang sederhana seperti dipanggang dan direbus, hingga teknik yang lebih rumit. Beberapa masakan bahkan dibuat dengan lebih dari satu teknik. Ada masakan yang dipanggang dahulu, kemudian direbus dan dipanggang lagi. Ada pula masakan yang direbus dahulu, kemudian digoreng. Semua teknik yang dilakukan memiliki tujuan untuk menjadikan hidangan lebih lezat. Secara umum, teknik memasak hidangan tradisional kita adalah teknik pengungkepan. Kita sangat bangga dengan kekayaan bumbu sehingga masakan harus terasa bumbunya.</font></p>
					<h3>alat yang digunakan</h3>
					<p align="justify"><font size="3">Alat masak kita sangat unik. Apalagi jika kita menggunakan alat masak yang asli. Ada masakan yang harus dimasak di dalam bumbu, di dalam belanga, atau dibungkus daun. Selain karena memanfaatkan kekayaan alam, alat masak tertentu memberi pengaruh aroma pada hidangan.</font></p>
					<h3>Daerah</h3>
					<p align="justify"><font size="3">
					<ol type="A">
						<li><b>Sumatera</b>
							<br><p align="justify"><font size="3">Masakan Sumatera dikenal dengan rempahnya yang harum dan kadang pedas. Beberapa hidangan juga bersantan. Paduan bahan seperti itu menciptakan hidangan yang tak mudah dilupakan.</font></p>
						</li>
						<li><b>Kalimantan</b>
							<br><p align="justify"><font size="3">Cara memasak yang unik bisa Anda temukan dalam kuliner Kalimantan. Sebut saja ketika membuat bubur, beras tidak dimasak utuh tetapi diblender terlebih dahulu. Belum lagi sayurannya yang sangat banyak.</font></p>
						</li>
						<li><b>Sulawesi</b>
							<br><p align="justify"><font size="3">Kuliner Sulawesi sungguh menarik. Ada daerah yang sangat suka hidangan pedas seperti Manado. Ada pula daerah yang suka hidangan manis. Keharuman bumbu dedaunan juga banyak dimanfaatkan dalam sebagian hidangan mereka. Unik dan lezat adalah kata yang pantas untuk menggambarkan kuliner orang Sulawesi.</font></p>
						</li>
						<li><b>Jawa</b>
							<br><p align="justify"><font size="3">Karena pulau Jawa begitu luas, maka tak heran jika kulinernya sangat beragam. Orang Jawa Barat memiliki selera yang berbeda dengan orang Jawa Tengah maupun orang Jawa Timur. Ada yang suka hidangan pedas, ada pula yang suka hidangan manis. Meski berbeda, hidangan-hidangan tersebut merupakan hidangan yang lezat dan memiliki ciri khas tersendiri.</font></p>
						</li>
						<li><b>Bali NTB NTT</b>
							<br><p align="justify"><font size="3">Karena letaknya yang berdekatan, terkadang ada masakan orang Bali yang memiliki kesamaan nama dengan masakan orang Lombok, misalnya. Namun meski memiliki kesamaan nama, ada saja sentuhan yang berbeda.</font></p>
						</li>
							
