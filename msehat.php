					<p align="justify"><font size="3">Dengan memanfaatkan tips berikut, Anda dapat mengubah sendiri resep kesukaan Anda menjadi hidangan yang lebih sehat.</font></p>
					<h3>Mengurangi Lemak Total</h3>
					<p align="justify"><font size="3">
					<ol>
						<li><p align="justify"><font size="3">Pilih bagian daging yang tidak berlemak. Buang sebanyak mungkin lemak dari daging sapi maupun daging ayam. Selalu buang kulit ayam yang serat lemak dan semua lemak yang tersembunyi.</font></p></li>
						<li><p align="justify"><font size="3">Perhitungkan waktunya jika membuat kaldu. Beri cukup waktu untuk membekukan lemak di permukaan sehingga mudah diangkat.</font></p></li>
						<li><p align="justify"><font size="3">Ganti santan dengan susu rendah lemak atau susu kedelai yang lebih aman kandungan lemaknya. Begitu pula dengan yoghurt, pilihlah yang rendah lemak.</font></p></li>
						<li><p align="justify"><font size="3">Batasi pemakaian minyak untuk menumis bumbu.</font></p></li>
						<li><p align="justify"><font size="3">Hindari menggoreng makanan. Gunakan cara-cara yang lebih sehat, seperti merebus, mengkukus, atau memanggang.</font></p></li>
					</ol>
					</font></p>
					
					<h3>Mengurangi kolesterol dan asam lemak jenuh</h3>
					<p align="justify"><font size="3">
					<ol>
						<li><p align="justify"><font size="3">Tingkatkan konsumsi protein nabati dari tempe dan tahu. Sayuran jenis polong-polongan dan biji-bijian seperti kacang merah, kacang polong, dan buncis sesungguhnya kaya protein nabati.</font></p></li>
						<li><p align="justify"><font size="3">Kurangi mengkonsumsi telur, hati, ginjal, otak, dan udang. Semua bahan ini kaya kolesterol LDL (<i>Low Density Lipoprotein</i>) yang tidak baik untuk kesehatan jantung.</font></p></li>
						<li><p align="justify"><font size="3">Sebisanya gunakanlah minyak zaitun, minyak <i>canola</i>, minyak jagung, atau minyak biji bunga matahari yang mengandung asam lemak tidak jenuh tunggal (<i>monounsaturated fats</i>) dan asam lemak tidak jenuh jamak (<i>polyunsaturated fats</i>). Gunakan dalam jumlah terbatas.</font></p></li>
						<li><p align="justify"><font size="3">Kurangi pemakaian santan. Jika memang benar-benar diperlukan untuk memberi rasa gurih, kurangi sampai tinggal seperempat atau setengahnya saja lalu tambahkan susu atau yoghurt rendah lemak atau tanpa lemak.</font></p></li>
					</ol>
					</font></p>
					
					<h3>Meningkatkan Serat</h3>
					<p align="justify"><font size="3">
					<ol>
						<li><p align="justify"><font size="3">Tambahkan sayuran yang kaya serat pada masakan Anda. Banyak masakan yang bisa dikurangi dagingnya, lalu diganti dengan kacang-kacangan atau polong-polongan. Rasanya akan tetap nikmat.</font></p></li>
						<li><p align="justify"><font size="3">Variasikan sayuran dan buah yang dikonsumsi</font></p></li>
						<li><p align="justify"><font size="3">Santaplah nasi merah yang kaya serat dan kaya nutrisi dibandingkan beras putih yang sudah dibuang kulit arinya. Jika sulit mengubahnya seketika, lakukan secara bertahap. Pada tahap penyesuaian campur beras merah dengan beras putih dulu, sajikan 3 kali seminggu.</font></p></li>
					</ol>
					</font></p>
					
					<h3>Mempertahankan nutrisi</h3>
					<p align="justify"><font size="3">
					<ol>
						<li><p align="justify"><font size="3">Sebisanya makanlah sayuran dalam keadaan mentah karena nutrisinya paling tinggi.</font></p></li>
						<li><p align="justify"><font size="3">Jangan merendam sayuran dalam air terlalu lama. Banyak mineral dan vitamin larut air yang akan hilang, misalnya vitamin C.</font></p></li>
						<li><p align="justify"><font size="3">Masak sayuran sesingkat mungkin. Panas api dapat merusak vitamin. Tutuplah panci saat memasak agar vitamin C tidak sempat "lari".</font></p></li>
						<li><p align="justify"><font size="3">Jangan menambahkan soda kue atau <i>baking powder</i> saat memasak sayuran. Vitamin C akan rusak oleh bahan tersebut.</font></p></li>
						<li><p align="justify"><font size="3">Jangan melelehkan daging atau ikan dalam air. Mineral dan vitamin yang larut air akan hilang. Jangan ragu memanfaatkan jus yang keluar dari daging. Tambahkan pada sup, makasakn gulai dan kari.</font></p></li>
					</ol>
					</font></p>
					
